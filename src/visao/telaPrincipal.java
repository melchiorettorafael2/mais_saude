    private void jButtonAgendaPacientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgendaPacientesActionPerformed
      
           try { 
            conecta.executaSql("select *from usuarios where usu_nome='"+jLabel2.getText()+"'");
            conecta.rs.first();
            if(conecta.rs.getString("usu_tipo").equals("Médico")||conecta.rs.getString("usu_tipo").equals("Administrador")){
             if(telaAgendaMédico==null){
                telaAgendaMédico = new FormAgendaMédico();
                telaAgendaMédico.setVisible(true);
             }else{
                 telaAgendaMédico.setVisible(true);
             }   
                
            }else{
                JOptionPane.showMessageDialog(rootPane,"Necessária a Permissão de Médico para Acessar a Agenda de Pacientes");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Necessária a Permissão de Médico para Acessar a Agenda de Pacientes");
        }
        
    }//GEN-LAST:event_jButtonAgendaPacientesActionPerformed

