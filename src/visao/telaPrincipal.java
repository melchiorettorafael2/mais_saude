    private void jMenuItemRelPacientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemRelPacientesActionPerformed
      
     Connection con = conexaoBD.conexao();
      
      
      int confirm = JOptionPane.showConfirmDialog(null, "Confirma a Impressão do Relatório?", "Atenção",JOptionPane.YES_NO_OPTION );
      if (confirm== JOptionPane.YES_OPTION){
          InputStream caminhoRelatorio = this.getClass().getClassLoader().getResourceAsStream("relatorios/relatoriodePacientes.jasper");
           JasperPrint jasperPrint= null;
          
          try {
              jasperPrint = JasperFillManager.fillReport(caminhoRelatorio, null, con);
              
          } catch (Exception ex) {
              JOptionPane.showMessageDialog(rootPane,"Erro ao Gerar Relatório!\n"+ex);
          }
          JasperViewer view = new JasperViewer (jasperPrint, false);
          view.setVisible(true);
          
      }
      
    }//GEN-LAST:event_jMenuItemRelPacientesActionPerformed

