    private void jTableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUserMouseClicked
         String usu_nome=""+jTableUser.getValueAt(jTableUser.getSelectedRow(),1);
        conex.conexao();
        conex.executaSql("select *from usuarios where usu_nome='"+usu_nome+"'");
        try {
            conex.rs.first();
            jTextFieldCod.setText(String.valueOf(conex.rs.getInt("usu_cod")));
            jTextFieldNomeUser.setText(conex.rs.getString("usu_nome"));
            jComboBoxUser.setSelectedItem(conex.rs.getString("usu_tipo"));
            jButtonEditarUser.setEnabled(true);
            jButtonExcluirUser.setEnabled(true);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao Selecionar os Dados!\n"+ex.getMessage());
            
        }
        conex.desconecta();
    }//GEN-LAST:event_jTableUserMouseClicked

