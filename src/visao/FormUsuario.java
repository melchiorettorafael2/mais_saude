     public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID", "Usuário", "Tipo"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
        
            do{
                dados.add(new Object[]{conex.rs.getInt("usu_cod"), conex.rs.getString("usu_nome"),conex.rs.getString("usu_tipo")});
            }while(conex.rs.next());//Enquanto houver dados ele vai percorrer o registro.
        }catch (SQLException ex){
            //SJOptionPane.showMessageDialog(rootPane,"Erro ao Preencher ArrayList\n"+ex.getMessage());   
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas) {};
        
        jTableUser.setModel(modelo);
        jTableUser.getColumnModel().getColumn(0).setPreferredWidth(35);
        jTableUser.getColumnModel().getColumn(0).setResizable(false);
        jTableUser.getColumnModel().getColumn(1).setPreferredWidth(210);
        jTableUser.getColumnModel().getColumn(1).setResizable(false);
        jTableUser.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableUser.getColumnModel().getColumn(2).setResizable(false);
        jTableUser.getTableHeader().setReorderingAllowed(false);
        jTableUser.setAutoResizeMode(jTableUser.AUTO_RESIZE_OFF);// Bloqueia Tabela para redimensionamento.
        jTableUser.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);// Usuário só pode selecionar um campo da tabela por vez.
        conex.desconecta();
    }
    

    
    
    
    
    
